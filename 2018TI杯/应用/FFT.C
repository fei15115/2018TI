void FFT( void )//1024点 基2快速傅里叶算法
{ 
	int i,b,k,j,p;			//中间变量    
	long int Temp_Real,Temp_Imag,temp ;          
  for(i=1; i<=10; i++) {          /* for(1)第一重循环,循环变量i */    
    b=1;
    b <<=(i-1);                //b为蝶形计算的两算子之间的下标间隔
    for( j=0; j<=b-1; j++) {   /* for (2)第二重循环,循环变量j *///一次完整的蝶形计算的区块数                                 
      p=1;                     //对于1024点FFT，每次蝶形计算共1024次,等于区块数*内部点数
      p <<= (10-i);           
      p = p*j;
      for( k=j; k<1024; k=k+2*b) { /* for (3)第二重循环,循环变量k*///每个区块里的计算次数       
        Temp_Real =Fft_Real[k];
		    Temp_Imag =Fft_Image[k];
			  temp      =Fft_Real[k+b];
        Fft_Real [k]  =Fft_Real[k] +(((long int)Fft_Real[k+b]*COS_TAB[p])>>15)+(((long int)Fft_Image[k+b]*SIN_TAB[p])>>15);
        Fft_Image[k]  =Fft_Image[k]-(((long int)Fft_Real[k+b]*SIN_TAB[p])>>15)+(((long int)Fft_Image[k+b]*COS_TAB[p])>>15);
        Fft_Real [k+b]=Temp_Real   -(((long int)Fft_Real[k+b]*COS_TAB[p])>>15)-(((long int)Fft_Image[k+b]*SIN_TAB[p])>>15);
        Fft_Image[k+b]=Temp_Imag   +(((long int)temp         *SIN_TAB[p])>>15)-(((long int)Fft_Image[k+b]*COS_TAB[p])>>15); // 移位.防止溢出.              
        Fft_Real[k]   >>= 1;  Fft_Image[k]  >>= 1;  Fft_Real[k+b] >>= 1;  Fft_Image[k+b]>>= 1; }                          //各项都除以2                           
      }   
    }     
  }
	